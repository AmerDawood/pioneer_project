class Initiative {
  final int id;
  final String title;
  final String organization;
  final String description;
  final int participants;
  final String date;
  final String imageUrl;

  Initiative({
    required this.id,
    required this.title,
    required this.organization,
    required this.description,
    required this.participants,
    required this.date,
    required this.imageUrl,
  });
}